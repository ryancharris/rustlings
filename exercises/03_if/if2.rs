fn picky_eater(food: &str) -> &str {
    if food == "strawberry" {
        "Yummy!"
    } else if food == "potato" {
        "I guess I can eat that."
    } else {
        let disliked_foods: [&str; 3] = [
            "broccoli",
            "gummy bears",
            "literally anything",
        ];
        if disliked_foods.contains(&food) {
            "No thanks!"
        } else {
            "I don't know about that."
        }
    }
}

